    private void recentlyUnparkedBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recentlyUnparkedBtnActionPerformed
        clearTable();
        int intChoice = 0;
        String[] options = {"Last 24 Hours", "Last 7 Days", "Last 30 Days"};
        intChoice = JOptionPane.showOptionDialog(this, "Select Your Time Choice", "Time Choice", 0, 1, new ImageIcon(), options, intChoice);
        try {
            double totalCost = ParkingSystem.printUnparkedCars(intChoice, this, table);
            if (totalCost > 0) {
                totalCostLbl.setText("Total Cost");
                costLbl.setText(String.valueOf(totalCost));
            }
        } catch (Exception ex) {
            Logger.getLogger(InfoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_recentlyUnparkedBtnActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int row = table.getSelectedRow();
        if (row != -1) {
            String plateNo = (String)table.getValueAt(row, 1);
            if (plateNo != null && plateNo.equals("---")) {
                plateNo = (String)table.getValueAt(row, 3);
            }
            String path = "files\\parkedRideImages\\" + plateNo + ".jpg";
            if (new File(path).exists()) {
                imageLbl.setIcon(new ImageIcon(path));
            }
        }
    }//GEN-LAST:event_tableMouseClicked

    private void clearTable() {
        for (int i = 0; i < 200; i++) {
            for (int j = 0; j < 11; j++) {
                table.setValueAt("", i, j);
            }
        }
    }
    

