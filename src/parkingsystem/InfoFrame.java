    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        if (searchComboBox.getSelectedIndex() == 0) {
            String plateNo = JOptionPane.showInputDialog("Enter plate number (for bicycle, enter full name)");
            Rideable r = ParkingSystem.search(plateNo);
            if (r == null) {
                JOptionPane.showMessageDialog(this, "Ride Not Found!");
            } else {
                searchTextArea.setText(r.info());
                imageLbl.setIcon(new ImageIcon("files\\parkedRideImages\\" + plateNo + ".jpg"));
            }
        } else if (searchComboBox.getSelectedIndex() == 1) {
            String slotID = JOptionPane.showInputDialog("Enter slot ID");
            int index = 0;
            try {
                index = Integer.parseInt(slotID.substring(0, 1));
                int ID = Integer.parseInt(slotID.substring(2, 4));
                searchTextArea.setText(ParkingSystem.mySlots[index - 1][ID - 1].info());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Wrong slot ID!");
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

